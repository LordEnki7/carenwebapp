#!/usr/bin/env bash
# ============================================================
# C.A.R.E.N Alert вЂ” One-command deploy to carenalert.com
# Run this from the Replit SHELL tab (not a workflow):
#
#   bash scripts/deploy-to-dokploy.sh
#
# What it does:
#   1. Stages all current changes
#   2. Removes .github/workflows/ from staging AFTER add-all
#      (the PAT lacks 'workflow' scope вЂ” staged workflow files
#       will cause GitHub to reject the push)
#   3. Commits any staged changes
#   4. Pushes fresh-main в†’ github/main
#   5. Confirms the push succeeded and tells you what to do next
# ============================================================

set -e   # Stop immediately on any error

GREEN='\033[0;32m'; RED='\033[0;31m'; YELLOW='\033[1;33m'; BOLD='\033[1m'; RESET='\033[0m'

echo ""
echo -e "${BOLD}C.A.R.E.N Alert вЂ” Deploy to carenalert.com${RESET}"
echo "в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ"

# в”Ђв”Ђ Step 0: Clean stale git lock files в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# A leftover .git/index.lock or refs/heads/*.lock blocks ALL git
# operations вЂ” every previous failed deploy left this lurking.
echo -e "\n${YELLOW}Step 0 вЂ” Cleaning stale git locks...${RESET}"
LOCKS_REMOVED=0
for lock in .git/index.lock .git/config.lock .git/HEAD.lock .git/refs/heads/*.lock .git/refs/remotes/github/*.lock; do
  if [ -f "$lock" ]; then
    rm -f "$lock"
    LOCKS_REMOVED=$((LOCKS_REMOVED+1))
  fi
done
if [ "$LOCKS_REMOVED" -gt 0 ]; then
  echo -e "  ${YELLOW}вљ  Removed $LOCKS_REMOVED stale lock file(s) вЂ” git is now unblocked${RESET}"
else
  echo -e "  ${GREEN}вњ“ No stale locks вЂ” git is clean${RESET}"
fi

# в”Ђв”Ђ Step 1: Write unique deploy timestamp to source file в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# Docker's COPY layer cache is content-addressed. By writing a unique timestamp to
# client/src/buildTimestamp.ts on every deploy, the file content changes, guaranteeing
# Docker's COPY layer is a cache MISS в†’ Vite rebuilds from fresh source inside Docker.
# This permanently eliminates the "Docker cached COPY from April 13" bug.
echo -e "\n${YELLOW}Step 1 вЂ” Stamping deploy timestamp into source code...${RESET}"
BUILD_TS=$(date -u '+%Y-%m-%dT%H:%M:%SZ')
cat > client/src/buildTimestamp.ts << EOF
// AUTO-GENERATED by scripts/deploy-to-dokploy.sh вЂ” DO NOT EDIT MANUALLY
// This file's content changes on every deploy to bust Docker's layer cache.
// Docker's COPY layer is content-addressed; a changed file guarantees a cache miss,
// forcing a fresh COPY + Vite rebuild inside Docker on every deployment.
export const DEPLOY_TIMESTAMP = "$BUILD_TS";
EOF
echo -e "  ${GREEN}вњ“ buildTimestamp.ts stamped: $BUILD_TS${RESET}"

# Also stamp the Dockerfile RUN echo line for belt-and-suspenders cache busting
sed -i "s|RUN echo \"BUILD_TIMESTAMP: .*\"|RUN echo \"BUILD_TIMESTAMP: $BUILD_TS\"|" Dockerfile
echo -e "  ${GREEN}вњ“ Dockerfile RUN echo stamped: $BUILD_TS${RESET}"

# в”Ђв”Ђ Step 1d: Build the frontend (Vite) in Replit where VITE_* secrets are available в”Ђв”Ђ
# Vite MUST run here, not in Docker вЂ” Docker build has no access to VITE_STRIPE_PUBLIC_KEY etc.
# The fresh dist/public/ is committed to git and copied into Docker via the fresh COPY layer
# (which is guaranteed to be a cache miss because buildTimestamp.ts changed above).
echo -e "\n${YELLOW}Step 1d вЂ” Building frontend with Vite (in Replit)...${RESET}"
if npx vite build 2>&1 | tail -3; then
  echo -e "  ${GREEN}вњ“ Frontend built вЂ” dist/public/ ready for deployment${RESET}"
else
  echo -e "  ${RED}вњ— Vite build failed вЂ” aborting deploy${RESET}"
  exit 1
fi

# в”Ђв”Ђ Step 1b: Write build-info.json with current git SHA, then stage в”Ђв”Ђв”Ђ
# build-info.json is the source of truth for "what commit is deployed".
# It gets committed, copied into the Docker image, served at /build-info.json,
# and exposed via /api/version.
echo -e "\n${YELLOW}Step 1b вЂ” Writing build-info.json + staging all changes...${RESET}"
node scripts/write-build-info.cjs
git add -A
CHANGED=$(git diff --cached --name-only | wc -l | tr -d ' ')
echo -e "  ${GREEN}вњ“ $CHANGED file(s) staged${RESET}"

# в”Ђв”Ђ Step 2: Remove workflow files AFTER staging в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# IMPORTANT: must come AFTER git add -A, otherwise add-all re-stages them
echo -e "\n${YELLOW}Step 2 вЂ” Removing .github/workflows/ from staging...${RESET}"
if git ls-files --cached .github/workflows/ | grep -q '.'; then
  git rm --cached .github/workflows/*.yml 2>/dev/null || true
  echo -e "  ${YELLOW}вљ  Workflow files removed from staging (PAT lacks workflow scope)${RESET}"
else
  echo -e "  ${GREEN}вњ“ No workflow files staged вЂ” safe to push${RESET}"
fi

# Confirm no workflow files remain staged
if git diff --cached --name-only | grep -q '.github/workflows'; then
  echo -e "  ${RED}вњ— Workflow files still staged! Forcing removal...${RESET}"
  git diff --cached --name-only | grep '.github/workflows' | xargs git rm --cached
fi

# в”Ђв”Ђ Step 3: Commit staged changes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "\n${YELLOW}Step 3 вЂ” Committing changes...${RESET}"
STAGED=$(git diff --cached --name-only | wc -l | tr -d ' ')
if [ "$STAGED" -gt 0 ]; then
  TIMESTAMP=$(date '+%Y-%m-%d %H:%M')
  git -c user.email="deploy@carenalert.com" -c user.name="CAREN Deploy" \
    commit -m "Deploy: $TIMESTAMP" --no-verify
  echo -e "  ${GREEN}вњ“ Committed $STAGED file(s)${RESET}"
else
  echo -e "  ${GREEN}вњ“ No new changes to commit вЂ” pushing existing commits${RESET}"
fi

# в”Ђв”Ђ Step 4: Check commits ahead of GitHub в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "\n${YELLOW}Step 4 вЂ” Checking commits ahead of GitHub...${RESET}"
git fetch github 2>/dev/null || true
AHEAD=$(git rev-list github/main..HEAD --count 2>/dev/null || echo "unknown")
echo -e "  ${GREEN}вњ“ $AHEAD commit(s) ahead of github/main${RESET}"

# Show the latest local commit that will be pushed
LATEST_MSG=$(git log HEAD --oneline -1)
echo -e "  Latest commit: ${BOLD}$LATEST_MSG${RESET}"

# в”Ђв”Ђ Step 5: Push to GitHub в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo -e "\n${YELLOW}Step 5 вЂ” Pushing to GitHub (triggers Dokploy)...${RESET}"

# Refresh the remote URL with the current PAT in case it expired
git remote set-url github "https://$GITHUB_PERSONAL_ACCESS_TOKEN2@github.com/LordEnki7/carenwebapp.git" 2>/dev/null || true

if git push github HEAD:main --no-verify; then
  PUSHED_SHORT=$(git rev-parse --short HEAD)
  echo ""
  echo -e "${GREEN}${BOLD}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${RESET}"
  echo -e "${GREEN}${BOLD}вњ“ PUSH SUCCESSFUL вЂ” code is on GitHub${RESET}"
  echo -e "${GREEN}${BOLD}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${RESET}"
  echo ""
  echo -e "  Commit pushed: ${BOLD}$PUSHED_SHORT${RESET}"
  echo ""
  echo -e "${YELLOW}${BOLD}Next steps to go live on carenalert.com:${RESET}"
  echo "  1. Open Dokploy dashboard"
  echo "  2. Click  в†’  Deploy  в†’  Rebuild without cache"
  echo "  3. Wait ~2 min for build to finish"
  echo "  4. Open https://carenalert.com/api/version вЂ” confirm serverStartTime changed"
  echo ""
  exit 0
else
  echo ""
  echo -e "${RED}${BOLD}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${RESET}"
  echo -e "${RED}${BOLD}вњ— PUSH FAILED${RESET}"
  echo -e "${RED}${BOLD}в”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓв”Ѓ${RESET}"
  echo ""
  echo "  Try refreshing the PAT and pushing again:"
  echo "    git remote set-url github https://\$GITHUB_PERSONAL_ACCESS_TOKEN2@github.com/LordEnki7/carenwebapp.git"
  echo "    git push github HEAD:main"
  echo ""
  echo "  If that still fails, your PAT may have expired вЂ” regenerate it in GitHub."
  exit 1
fi
